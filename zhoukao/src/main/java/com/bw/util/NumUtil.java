package com.bw.util;

public class NumUtil {
	
	public int add(int a,int b){
		int result = a+b;
		return result;
	}
	
	public  int jian(int a,int b){
		int result = a-b;
		return result;
	}
	
	public  int cheng(int a,int b){
		int result = a*b;
		return result;
	}
	
	public  int chu(int a,int b){
		int result = a/b;
		return result;
	}
}
